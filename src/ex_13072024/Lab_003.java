package ex_13072024;

public class Lab_003 {
    public static void main(String[] args) {
        //Data_Type: Primitive Examples!

        //Boolean Data Type
        boolean is_married = true;
        boolean is_not_married = false;
        System.out.println("is_married");
        System.out.println("is_not_married");

        // Numeric Data Type
        //A. Integral Data Type:
        byte b = 122;
        short s = 10;
        int i = 1995;
        long l = 8767496068L;
        char c= 'P';
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(c);

        //B. Floating point
        float f = 3.14f;
        double d = 112244.3332255;
        System.out.println(f);
        System.out.println(d);

        //Non_Primitive:

        String name1 = "Prajakta";
        String name2 = "Mahadik";
        System.out.println("My name is :" + name1 + " " +name2);

    }
}
